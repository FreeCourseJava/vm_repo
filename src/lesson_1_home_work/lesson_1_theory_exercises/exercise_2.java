// 2. int 1000000 умножить на себя, вывести, объяснить что происходит
package lesson_1_home_work.lesson_1_theory_exercises;

public class exercise_2 {
    public static void main(String[] args) {
        int justMillion = 1000000;
        int justMillionSquare = justMillion * justMillion;
        System.out.println("Результат возведения в квадрат миллиона: " + justMillionSquare);
    }
}

// В данном случае мы наблюдаем ситуацию многократного целочисленного переполнения.
// 1) Объявляем целочисленную переменную justMillion, равное 1000000
// 2) Объявляем целочисленную переменную justMillionSquare, которая представлет из себя результат умножения переменной justMillion на саму себя
// 3) В результате умножения значение переменной justMillionSquare выходит за предел диапазона объявленного типа данных
// 4) В результате целочисленного переполнения значение justMillionSquare:
//  4.1) значение возвращается к минимальному значению диапазона типа данных int -2147483648
//  4.2) также при этом происходит перенос "остатка" переменной justMillionSquare в диапазон типа данных int
//  4.3) поскольку "остаток" очень большой, шаги 4.1 и 4.2 повторяется до тех пор, пока "остаток" не будет укладываться в диапазон типа данных int (приблизительно 232 раза)

